package com.nathit.dashboardadmin.model;

public class ModelAdmin {

    String email;
    String uid;

    public ModelAdmin() {
    }

    public ModelAdmin(String email, String uid) {
        this.email = email;
        this.uid = uid;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }
}
